/*
功能描述    : 传入文件名，计算该文件的md5值
返回值      : 成功为0，失败为-1
参数        : @file_name 文件名
@md5_sum 将md5计算完成后通过该参数传出
作者        : 张彦升
日期        : 2014年2月17日 12:40:36
*/
uint32_t CIAlgorithm_Md5(const char* p_file_name,unsigned char md5_sum[MD5_DIGEST_LENGTH])
{
    MD5_CTX mdContext;
    int bytes;
    unsigned char data[1024];
    FILE *inFile = NULL;

    if (NULL == p_file_name)
    {
        return -1;
    }

    inFile = fopen (p_file_name, "rb");
    if (NULL == inFile)
    {
        return -1;
    }

    MD5_Init (&mdContext);
    while ((bytes = fread (data, 1, 1024, inFile)) != 0)
    {
        MD5_Update (&mdContext, data, bytes);
    }
    MD5_Final (md5_sum,&mdContext);

    fclose (inFile);

    return 0;
}

