/*
功能描述    : 得到两个数的最大公约数，暂时只支持无符号类型
返回值      : 
参数        : 
作者        : 
日期        : 2013年10月11日 13:32:26
*/
uint32_t CIAlgorithm_Gcd(uint32_t x, uint32_t y)
{
    uint32_t t = 0;
    if (!x || !y)
    {
        return x > y ? x : y;
    }
#ifdef WIN32
#   pragma warning(push)          // Saves the current warning state.
#   pragma warning(disable :4706) //  warning C4706: 条件表达式内的赋值
#endif /* WIN32*/

    for (; (t = x % y); x = y, y = t);

#ifdef WIN32
#   pragma warning(pop)          // Restores the warning state.
#endif /*WIN32*/

    return y;
}

/*
功能描述    : 求两个数的最小公倍数，该函数改编自多个求最小公倍数的算法，其效率并不是最高
             但是用在我们这里，却是最好的，
返回值      : 
参数        : 参数必须为两个16位的数字，如果传入一个有符号的数字，将会自动强制转换为无符号
             的16位数字
作者        : 张彦升
日期        : 2013年10月16日 9:49:22
*/
uint32_t CIAlgorithm_Lcm(uint16_t x,uint16_t y)
{
    uint16_t max_num = MAX(x,y);
    uint16_t min_num = MIN(x,y);

    uint32_t ans = max_num;
    uint32_t len = 2;
    uint32_t i = len - 1;

    if(0 == x || y == 0)
    {
        return 0;
    }

    while (i > 0)
    {
        i -= 1;
        if (ans % min_num != 0)
        {
            ans += max_num;
            i  = len - 1;
        }
    }

    return ans;
}

/*
功能描述    : 该函数来自苹果网站，需要完整验证，主要验证对不通CPU上述CRC静态表是否正确
返回值      : 
参数        : 
作者        : 张彦升
日期        : 2013年10月24日 15:11:28
*/
static uint32_t crc32(uint32_t crc, const void* p_buf, uint32_t size)
{
    const uint8_t *p;

    p = (const uint8_t*)p_buf;
    crc = crc ^ ~0U;

    while (size--)
    {
        crc = crc32_tab[(crc ^ *p++) & 0xFF] ^ (crc >> 8);
    }

    return crc ^ ~0U;
}

/*
功能描述    : 对crc32函数进行一次包装，提供我们自己的crc版本
返回值      : 计算出来的crc32值
参数        : buf需要计算crc的数据头指针，size为该数据的长度。
作者        : 张彦升
日期        : 2013年10月24日 15:13:05
*/
uint32_t CIAlgorithm_Crc32(const void* p_buf,uint32_t size)
{
    uint32_t crc = 0;
    crc = crc32(crc,p_buf,size);

    return crc;
}
/*
 功能描述    : 计算16位的crc
 返回值      : 成功为0，失败为-1
 参数        : 
 作者        : 何境泰
 日期        : 2014年7月28日 13:42:26
*/
uint16_t CIAlgorithm_Crc16(const void* data, uint32_t size)
{
    uint32_t i = 0;
    uint16_t  acc = 0;
    uint8_t  a = 0, h = 0, l = 0;
    const uint8_t* p_data = (uint8_t*)data;

    /*查表发计算CRC码*/
    for( i = 0;  i < size; i++ )
    {
        a = ((uint8_t)(acc >> 8 ) ^ ( *p_data));
        h = crc_tbl_high[ (a & 0x00ff) ];
        l = (uint8_t)( acc & 0x00ff );
        h = h ^ l;
        l = crc_tbl_low[ (a & 0x00ff) ];
        acc = (((uint16_t)h) << 8 ) | (((uint16_t)l) & 0x00ff);

        p_data++; 
    }
    return acc;
}

